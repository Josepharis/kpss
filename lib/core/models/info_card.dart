class InfoCard {
  final String id;
  final String title;
  final String description;
  final String icon;
  final String color;
  final String topicId;
  final String lessonId;
  final int cardCount;

  InfoCard({
    required this.id,
    required this.title,
    required this.description,
    required this.icon,
    required this.color,
    required this.topicId,
    required this.lessonId,
    required this.cardCount,
  });
}

